<h1>Group Risk Plan (GRP)</h1>

<p>A <b>Group Risk Plan (GRP)</b> provides a dollar amount of protection per acre. A loss payment triggers when the county average yield in a given year falls below the trend adjusted average yield by a greater percentage than the insured's selected deductible. GRP does not provide prevented planting, late planting or replant payments.</p>

<p><b>Levels of Coverage</b><br />
The grower selects the dollar amount of protection per acre and the percentage of the county yield (70% to 90% for most crops) at which he/she wants to insure. GRP Catastrophic (CAT) coverage is available at 65% of the expected county yield and 45% of the maximum amount of protection per acre.</p>

<p><b>Units</b><br />
The coverage unit is all acreage of the crop in the county.</p>

<p><b>Benefits</b><br />
GRP is a simplified program that is easy to understand. GRP provides payment based on performance of a crop in a county as a whole. GRP is more appealing than a CBOT crop yield contract because: ◦	GRP triggers based on county, rather than state, average yield. ◦	The grower's transaction cost is lower because of the government cost share.</p>

<p><b>Loss Payment</b><br />
A loss is payable when the county average yield for the crop in the current year is less than the percentage of coverage selected by the grower at the time of application.</p>

