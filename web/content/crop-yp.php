<h1>Yield Protection (YP)</h1>

<p><b>Yield Protection (YP)</b> provides prevented planting and replant protection and protects against a production loss for crops for which revenue protection is available but was not selected. Coverage is expressed as a production guarantee (approved yield times the coverage level) and is available in 5 percent increments from 50 percent to 75 percent (80 percent and 85 percent coverage levels are available in limited areas) of the approved yield up to 100percent of the projected price. The projected price is determined by the Commodity Exchange Price Provisions. </p>

<p><b>Catastrophic (CAT)</b> coverage is available at 50percent of the approved yield and 55percent of the projected price (50/55).</p>

<p><b>Yield Guarantee</b><br />
The YP yield guarantee is the approved yield multiplied by the selected level of coverage and the insured acreage. </p>

<p><b>Production to Count</b><br />
The production to count for the insurance unit is the actual production, plus any yield appraisals, less any adjustments for excess moisture or poor quality (if applicable). Clients should contact Federal Crop Insurance Agency if the crop is damaged or if plans exist to utilize production in such a way that harvested production cannot be determined.</p>

<p><b>Loss Payment</b><br />
The loss payment is calculated by subtracting the net amount of production from the yield guarantee and multiplying the result by the projected price and percent of share.</p>

<p><b>About Units</b><br />
The basic unit is all acreage of the crop in the county of which the insured has 100 percent ownership or shares with the same person. Basic units may be further divided into optional units, pending qualifications. Enterprise units are available for all crops with YP. The unit structure defined in the Common Crop Insurance Provisions may be modified by the Crop Provisions.</p>


