<h2><a name="cattle">Cattle</a></h2>

<p><b>About LGM for Cattle</b><br />
LGM provides protection against loss of gross margin (market value of cattle minus feeder cattle and feed costs) on cattle. LGM covers a decline in cattle prices and/or an increase in feed costs and/or an increase in feeder cattle prices.</p>

<p><b>LGM Eligibility</b><br />
Cattle producers in CO, IL, IN, IA, KS, MI, MN, MO, MT, NE, NV, ND, OH, OK, SD, TX, UT, WI, WV and WY feeding cattle in these states are eligible for LGM. Producers must have an ownership share in the cattle being produced.</p>

<p><b>LGM Coverage Levels</b><br />
Producers can choose deductible amounts from $0 to $150 per head, in $10 increments.</p>

<p><b>Determining Coverage</b><br />
First, determine whether the operation is a yearling to finish or a calf to finish. Next, determine the number of cattle to be marketed each month of the insurance period, then sum the ten monthly expected gross margin amounts and subtract the applicable deductible (deductible per head x sum of target marketings) to obtain the insurance period gross margin guarantee:</p>

<ul>
<li><b>Yearling to Finish</b></li>
<li>Expected Gross Margin per Head = (12.50 x Live Cattlet) - (7.50 x Feeder Cattlet-5) - (50.0 x Cornt-2)</li>
<li><b>Calf to Finish</b></li>
<li>Expected Gross Margin per Head = (11.50 x Live Cattlet) - (5.50 x Feeder Cattlet-8) - (52.0 x Cornt-4)</li>
</ul><br />

<p><b>LGM Coverage Period and Restrictions</b></p>

<ul>
<li>LGM has 12 insurance periods per calendar year.</li>
<li>Target marketings cannot be insured in the first month of the period.</li>
<li>Price risk protection lasts for eleven months (e.g., Jan. 31 sales closing date covers Feb. [no coverage in Feb.] - Dec.).</li>
<li>Price guarantees are based on futures prices and are set the last business Friday of each month.</li>
<li>Sales period begins the last business Friday of each month until 8:00 p.m. CST the following evening.</li>
<li>Covers up to 5,000 head during any 11-month insurance period and up to 10,000 head per crop year.</li></ul><br />

<p><b>Loss Payments</b></p>

<ul>
<li>Calculate the actual gross margin using the last three trading days prior to each contract's expiration date.</li>
<li>Subtract the total actual gross margin from the gross margin guarantee to obtain the loss payment.</li>
<li>The price at which cattle are sold does not affect the loss payment.</li>
<li>Loss payments will be prorated if actual marketings fall below 75% of target marketings.</li>
</ul><br />

