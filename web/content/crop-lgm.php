<h2><a name="dairy">Dairy</a></h2>

<p><b>About LGM-Dairy</b><br />
Livestock Gross Margin for Dairy Cattle provides protection against the loss of gross margin (market value of milk minus feed costs) on the milk produced from dairy cows.</p>

<p><b>LGM-Dairy Eligibility</b><br />
Any producer who owns dairy cattle in AL, AR, AZ, CA, CO, CT, DE, FL, GA, ID, IL, IN, IA, KS, KY, LA, ME, MD, MA, MI, MN, MO, MS, MT, NC, NE, NV, NH, NJ, NM, NY, ND, OH, OK, OR, PA, RI, SC, SD, TN, TX, UT, VA, VT, WA, WV, WI or WY is eligible for LGM.</p>

<p><b>LGM-Dairy Deductibles</b><br />
Producers can select deductible amounts from $0 to $2 per hundredweight of milk, in $0.10 increments.</p>

<p><b>Determining Coverage for LGM-Dairy</b><br />
Producers will need to determine the number of hundredweight of milk to be marketed and insured in each month of the insurance period. They will also need to determine the number of tons of corn or corn equivalent and the tons of soybean meal or soybean meal equivalent that they expect to feed for each month in which they insure milk. The number of tons of corn or corn equivalent must be between 0.00364 tons (7.28 lbs) and 0.0381 tons (76.2 lbs) per hundredweight of milk. The number of tons of soybean meal or soybean meal equivalent must be between 0.000805 tons (1.61 lbs) and 0.013 tons (26 lbs) per hundredweight of milk. Producers may also choose to use default values of 0.014 tons of corn (0.5 bushels) and 0.002 tons of soybean meal (4.0 lbs) per hundredweight of milk.</p>

<ul>
<li>Expected Gross Margin Per Month = Expected Revenue – Expected Cost of Feed for Month </li>
<li>Expected Revenue = Expected Milk Price x Target Marketings </li>
<li>Expected Cost of Feed = (Corn Tons x 2000/56 x Expected Corn Price) + (Soybean Meal x Expected Soybean Meal Price)</li>
</ul><br />

<p><b>LGM-Dairy Coverage Period and Restrictions</b></p>

<ul>
<li>12 insurance periods per year.</li>
<li>11months each.</li>
<li>No milk can be insured in the first month of the insurance period.</li>
<li>Coverage begins one full calendar month following the sales closing date.</li>
<li>Sales period begins the last business Friday of each month until 8:00p.m. CST the following evening.</li>
<li>Covers up to 240,000 hundred weight of milk in any insurance period or crop year.</li>
<li>Premium for LGM-Dairy is due at the end of the insurance period.</li>
<li>A premium subsidy will be available for those policies that insure multiple months during the insurance period. The subsidy amount will be determined by a dollar
deductible selected by the insured (ranging from $0 to $2, in $0.10 increments). Insureds choosing a $0 deductible will receive a lower premium subsidy (18%) and those choosing higher deductibles of $1.10 to $2 will receive a higher premium subsidy (50%).</li>
</ul><br />

<p><b>LGM Loss Payments</b></p>
<ul>
<li>Calculate the actual gross margin using the last three trading days prior to each contract's expiration date. </li>
<li>Subtract the actual total gross margin from the gross margin guarantee to obtain the loss payment.</li>
<li>The price at which milk is sold does not affect the loss payment.</li>
<li>Loss payments will be pro-rated if actual marketings fall below 75% of target marketings.</li>
</ul><br />

