<h1>Livestock Gross Margin</h1>

<p><b>Livestock Gross Margin (LGM)</b> provides protection for swine, cattle and dairy against the loss of gross margin, which is based on the difference between the market value of the livestock or milk minus the feed costs.</p>

<h2><a name="swine">Swine</a></h2>

<p><b>About LGM for Swine</b><br />
LGM provides protection of the gross margin between the value of insured hogs and the cost of corn and soybean meal. LGM covers a decline in hog prices and/or an increase in feed costs.</p>

<p><b>LGM Eligibility</b><br />
Swine producers in all 48 contiguous states feeding hogs are eligible for LGM. Producers must have an ownership share in the hogs being produced.</p>

<p><b>LGM Coverage Levels</b><br />
Producers can choose deductible amounts from $0 to $20 per head, in $2 increments.</p>

<p><b>Determining Coverage</b><br />
First, determine whether the operation is a farrow to finish, a segregated early weaned (SEW) or a finishing operation. Next, determine the number of hogs to be marketed each month of the insurance period, then sum the five monthly gross margin amounts and multiply by the coverage level to obtain the insurance period gross margin guarantee:</p>

<ul>
<li><b>Farrow to Finish</b></li>
<li>Gross Margin per Month = 2.6 × 0.74 × Lean Hog Pricet – 12.0 bu. × Corn Pricet-3 – (138.55 lb./2000 lb.) × Soybean Meal Pricet-3 × Number of Hogs</li>
<li><b>SEW</b></li>
<li>Gross Margin per Month = 2.6 × 0.74 × Lean Hog Pricet – 9.05 bu. × Corn Pricet-2 – (91.0 lb./2000 lb.) × Soybean Meal Pricet-2 × Number of Hogs</li>
<li><b>Finish</b></li>
<li>Gross Margin per Month = 2.6 × 0.74 × Lean Hog Pricet – 9.0 bu. × Corn Pricet-2 – (82.00 lb./2000 lb.) × Soybean Meal Pricet-2 × Number of Hogs</li>
</ul>
<br />

<p><b>LGM Coverage Period and Restrictions</b></p>
<ul>
<li>12 insurance periods per calendar year. </li>
<li>Price risk protection lasts for six months (e.g., Jan. 31 sales closing date covers Feb. [no cov. in Feb.] - July).</li>
<li>Target marketings cannot be insured in the first month of the period. </li>
<li>Price guarantees are based on futures prices and are set on the last business Friday of each month. </li>
<li>Sales period begins the last business Friday of each month until 8:00 p.m. CST the following evening.</li>
<li>Covers up to 15,000 hogs during any six-month insurance period and up to 30,000 hogs per crop year.</li>
</ul>
<br />

<p><b>Loss Payments</b></p>
<ul>
<li>Calculate the actual gross margin using the last three trading days prior to each contract's expiration date. </li>
<li>Subtract the total actual gross margin from the gross margin guarantee to obtain the loss payment. </li>
<li>The price at which hogs are sold does not affect the loss payment. </li>
<li>Loss payments will be prorated if actual marketings fall below 75% of target marketings.</li>
</ul>
