<h1>Group Risk Income Protection (GRIP)</h1>

<p><b>The Group Risk Income Protection (GRIP)</b> plan of coverage provides insurance protection against an unexpected, widespread decline in revenues, based on county average yields rather than the actual yield of the farm or insured unit. It is an alternative to the traditional Actual Production History (APH) coverage.</p>

<p>The insured is paid when the county revenue falls below a selected trigger revenue. Coverage levels are available from 70 percent to 90 percent, in 5 percent increments of the county trigger revenue. Protection per acre is available from 60 percent to 100 percent of the county maximum protection per acre (expected county yield multiplied by expected price and 150 percent).</p>

<p><b>GRIP Trigger Revenue (Guarantee)</b><br />
The trigger revenue (guarantee) is the expected county yield multiplied by the expected price (the simple average of the daily settlement prices for the trading month on the crop futures contract specified in the Crop Provisions), level of coverage and insured acreage. A Harvest Revenue Option is available, which redefines the trigger revenue price as the greater of the expected price or the harvest price (see example below for more details).</p>

<p><b>Loss Payment</b><br />
The loss payment is calculated by multiplying the payment calculation factor (the trigger revenue minus the insured's county revenue, divided by the insured's trigger revenue) by the insured's protection per acre (60percent to 100percent of the maximum county protection per acre), insured acres and the insured's ownership share.</p>

<p>Benefits of GRIP</p>

<ul>
</li>
<li>No APH is required since GRIP is based on county yield rather than individual yield.</li>
<li>Protection for producers who have multiple properties throughout a county because it is contingent upon a reduction in the average county yield or commodity price.</li>
<li>The Harvest Revenue Option (HRO) values lost bushels at the harvest price in addition to the coverage offered under GRIP.</li>
</ul>
