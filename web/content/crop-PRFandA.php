<h1>Pasture, Rangeland, Forage and Apiculture Rainfall Index</h1>

<p><b>About Pasture, Rangeland, Forage and Apiculture Rainfall Index</b></p>

<p>Coverage under PRF is available for Grazingland and Hayland crops while coverage under API is available under the No Type Specified crop type. This risk management tool insures against widespread loss of production of the insured crop in a designated grid area rather than individual farms.</p>

<p>The program does not provide hail and fire exclusion, high-risk land, late planting, replant requirements, replanting payments, prevented planting or experience adjustment factor payments.
Protection factors allow individualization of coverage based on the production of the crops produced and may be selected from 60percent to 150percent, in 1percent increments.</p>

<p>Losses are paid when the grid's accumulated index, known as the final grid index, falls below the insured's trigger grid index. Indemnities are based on the deviation from normal for each grid. A payment may be made only if the final grid index for the insured unit is less than the grower's trigger grid index. The payment will be equal to the payment calculation factor multiplied by the policy protection per unit.</p>

<p><b>Grid ID</b><br />
The grid ID number, which corresponds with the location of the insured cropland, is determined by using the Risk Management Association (RMA) website. The grid is utilized to determine the expected grid index and the final grid index, which is used in premium and loss calculation. The grid ID number will be reported on the acreage report.</p>

<p><b>Rainfall Index - (RI) Pasture, Rangeland, Forage (PRF) and Apiculture (API)</b><br />
The insurance period is determined by the interval selected by the insured. The Special Provisions may contain minimum/maximum limitations on the percentage of acreage that may be insured in any one interval.</p>

<p><b>Rainfall Index Interval Periods</b><br />
The crop year begins January 1 and ends December 31. The rainfall index has 11 flexible, two-month time periods that cannot overlap. A minimum of two intervals (four months total) per crop year must be insured under the Rainfall Index program.</p>


