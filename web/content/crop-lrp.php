<h1>Livestock Risk Protection (LRP)</h1>

<p><b>Livestock Risk Protection</b> provides protection against declining livestock prices if the price, as specified in the policy, drops below the producer's selected coverage price.</b>

<p><b>LRP Coverage</b><br />
LRP covers a decline in livestock prices.</p>

<p><b>LRP Eligibility</b><br />
Producers in all covered states with an ownership share in eligible livestock (see chart below for details).</p>

<p><b>LRP Prices</b><br />
Coverage prices range from 70% to 100% of daily livestock prices for swine, fed cattle and feeder cattle; and 80% to 95% for lambs. LRP is priced and available for sale continuously throughout the year.</p>

<p><b>Determining Coverage for LRP</b><br />
Determine the number of livestock to be marketed and the target weight. Multiply the number of head by the target weight, coverage price and insured share.</p>

<p><b>LRP Coverage Period and Restrictions</b><br />
Livestock can be insured for various different weekly increments (see chart below for details).</p>

<p><b>Loss Payments</b></p>

<ul>
<li>Multiply the number of head by the cwt target weight.</li>
<li>Subtract the actual ending value from the coverage price (loss payment due if positive).</li>
<li>Multiply the target weight times the difference between the actual ending value and the coverage price.</li>
<li>Multiply by the insured share.</li>
<li>The price at which livestock is sold does not affect the loss payment.</li>
</ul><br />

<p><b>Benefits of LRP</b></p>
<ul>
<li>Guaranteed price</li>
<li>No bid/ask spread.</li>
<li>Limited basis risk coverage</li>
<li>The aggregate cash price used better reflects actual price received.</li>
<li>Any number of head can be covered (up to limits)</li>
<li>Numerous endorsement period options</li>
<li>Producer selects the period that fits his/her risk management plan.</li>
<li>Wider range of target weights than CME</li>
<li>LRP is an insurance policy</li>
<li>LRP may be viewed more favorably by lenders than hedging or speculating (derivative products).</li>
</ul><br />

