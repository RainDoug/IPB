<h1>Crop Hail</h1>

<p><b>Crop-Hail</b>coverage provides acre-by-acre protection against physical damage from hail and/or fire and may be used as loan collateral. Crop-Hail can be used along with other comprehensive Federal Crop Insurance Agency coverages to offset the deductible and provide protection up to the actual cash value of the crop. Even partial damage of a property may be eligible for payment when the rest of the unit remains unaffected. Increases in coverage during the growing season are allowed to cover the value of the crop should bumper crop yields or higher prices surface. </p>

<p>Other coverages include fire department service charges, transit coverage to the first place of storage, catastrophe loss award (most coverages) and replanting coverage (most crops). Incidents such as wind and theft may also be covered under some policies. </p>

<p>Crop-Hail can be used along with MPCI or other comprehensive coverages to offset the MPCI deductible and provide protection up to the actual cash value of the crop. Coverage is provided on an acre-by-acre basis, so damage that occurs on only part of a farm may be eligible for payment when the rest of the unit remains unaffected.</p>

<p>If a grower has coverage and bumper crop yields or higher prices become apparent, coverage can be increased during the growing season to cover the value of the crop.</p>

<p><b>Dollar Guarantee</b><br />
A dollar amount of coverage per acre is selected by the grower. Options with different deductibles may be selected to permit a grower to partially self-insure for reduced premium costs.</p>

<p><b>Loss Payment</b><br />
To calculate a payable loss, multiply the amount of coverage per acre applying on the date of loss by the damaged acreage and the percentage of loss, less any deductibles.<p>


