<h1>Prevented Planting Coverage (PPC)</h1>

<p><b>Prevented Planting Coverage</b> provides payment for up to two years to growers when they are unable to plant their crops due to an insurable cause such as extreme weather, including drought. Applies to all crops, unless otherwise specified.</p>
 
<p><b>Dollar Guarantee</b><br />
The guarantee is the protection per acre for timely planted acreage. Depending on location, additional prevented planting coverage levels of plus 5 percent and plus 10 percent are available.</p>

<p><b>Production to Count</b><br />
Acreage prevented from being planted must remain idle or be planted to a cover crop (not intended to be hayed, grazed, or otherwise harvested, or is hayed or grazed on or after November 1 of the current crop year) to be eligible for 100 percent of the applicable prevented planting payment. Production from planted acres in the unit does not count against the prevented planting guarantee (indemnity). If a producer chooses to plant a second crop for harvest after the late plant period for the first crop (and it does not meet the double- cropping requirements), the first crop prevented planting payment will be reduced to 35percent of the applicable prevented planting payment and a yield equal to 60percent of the approved yield for the first insured crop will be used to calculate the average yield for subsequent crop years for the first insured crop.</p>

<p><b>Maximum Eligible Prevented Planting Acres by Crop and County</b><br />
Maximum eligible prevented planting crop acreage is limited to the maximum number of acres certified for Actual Production History (APH) purposes or reported for insurance for the crop in any one of the four most recent calendar years (or the number of acres specified in a processor contract). If an insured has not produced any crop for which insurance was available in any of the four most recent calendar years, eligible acres will be calculated as the number of acres specified on an intended acreage report (not to exceed the number of acres of cropland in the farming operation). Eligible acres may be increased if land is added to the farming operation, i.e., purchase or lease of additional land or acreage released from Conservation Reserve Program (CRP).</p>

<p><b>Payment Limitations</b><br />
To be eligible, first, the acreage that is prevented from planting must be insurable and available for planting and proof must be provided that inputs to produce the crop are available and that the crop was previously planted or prevented from planting. Second, the prevented planting acreage must be at least the lesser of 20 acres or 20 percent of the insurable crop acreage in the unit. If different crops are planted within the same field, or prevented planting is claimed on a different crop than what is planted in the same field, the producer must have records to support that similar plantings occurred in the four most recent crop years.</p>

<p><b>Reporting Changes or Crop Damage</b><br />
Producers should notify Federal Crop Insurance Agency if the producer wants to make a change in the level of coverage (by sales closing date) or if there is a change in the farm operation, such as added land or if the crop is not timely planted. </p>

