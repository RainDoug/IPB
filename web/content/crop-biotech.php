<h1>Biotechnology Endorsement</h1>

<p><b>The Pilot Biotechnology Endorsement (BE)</b> provides a premium rate reduction if at least 75percent of the acres in each unit of corn for grain is planted to a qualifying hybrid.</p>

<p><b>Availability</b><br />
This endorsement is available in all Colorado, Nebraska, South Dakota counties in which an eligible policy and plan of insurance is offered. Restrictions do apply, so let <? echo $businessName;?> help you determine if this program the right fit for you.</p>

<p><b>Eligibility Requirements</b><br />
Applicants for BE are required to purchase a Yield Protection, Revenue Protection or Revenue Protection with Harvest Price Exclusion plan of insurance at a coverage above the catastrophic 50 percent level. Additionally, 75 percent of their corn acres of an insured unit must contain the specific corn hybrid trait combinations as specified. All other program requirements must be met to realize a rate reduction, including U.S. Environmental Protection Agency (EPA) refuge acre requirements. Insureds should see their seed dealer for refuge planting details.</p>

<p><b>How It Works</b><br />
Crop insurance premiums will be reduced by the BE rate reduction when an insured meets all pilot program requirements, reports eligible units of insured corn to his/her crop insurance agent on or before the respective acreage reporting date and offers assistance if selected for mandatory review per regulations.</p>

<p><b>BE Premium Discount</b><br />
12 percent to 24 percent premium rate reductions are calculated from a producer's average yield history as well as the plan and level of crop insurance coverage elected. The discount will be a higher percentage for lower coverage level policies and lower percentage for higher coverage level policies. The premium rate reduction applies only to the yield portion of the crop insurance premium (excluding the revenue rate factors).</p>

<p><b>Hybrid Eligibility</b><br />
A chart detailing qualifying hybrids can be found below. Additionally, in order to be a qualifying hybrid, the corn hybrid planted must be seed that was obtained for planting in the crop year in which the premium rate reduction will be applied. Corn hybrids planted using seed that was obtained for planting in a prior crop year do not qualify as a qualifying hybrid for the current crop year. (For example, corn hybrid seed that was obtained for planting in the 2009 crop year, but was not planted until the 2010 or subsequent crop year, is not a qualifying hybrid for 2010 or subsequent crop years.)</p>

