	<p></p>
	<b>SHORT TERM CANCELLATION:</b>  Cancellation of an insurance policy prior to the expiration date in which a penalty in the form of a less than full pro-rata premium refund is allowed. 
	<p></p>
	<b>SINGLE INTEREST INSURANCE:</b>  Insurance coverage for only one of the part ie shaving an insurable interest in that property.  For instance, if a policy holder still owes money on his mortgage, and he does not have homeowners insurance, the lender may take out a single interest insurance policy to protect its own interest in the property.  Single interest insurance protects only the policy owner, not the homeowner. 
	<p></p>
	<b>SPECIAL CLASS:</b> Policies on which an extra premium rate is charged because an extra risk is presented. 
	<p></p>
	<b>STANDARD PROVISIONS:</b> Policy provisions required by law or supervisory regulation –such as provisions relating to grace period and incontestability. 
	<p></p>
	<b>STOCK COMPANY:</b> A company organized and owned by stockholders, as distinguished from the mutual form of company which is owned by its policyholders. 
	<p></p>
	<b>SUBROGATION:</b>  Assignment of rights of recovery from insured. 

