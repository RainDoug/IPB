	<a name=a>
	<div class="glossarytitle"> &nbsp;<b>A</b><a href=#home class=backtotop >back to top</a></div>
	<p></p>
	<b>ACCIDENT:</b> An event causing loss, which occurs without being expected or designed, usually specific in time and place. 
	<p></p>
	<b>ACQUISITION COSTS:</b> Expenses incurred in acquiring new business premiums and conservation of renewal business. Broad in scope, it includes cost of soliciting business, issuance of policies, collection of premiums, agents' compensation, field supervision, advertising, and any other expense reasonably attributable to acquisition and conservation of written premiums. 
	<p></p>
	<b>ACTUAL CASH VALUE (ACV):</b> The cost of replacing or restoring property at prices prevailing at the time and place of the loss, less depreciation, however caused. Another definition: the sum of money required to replace property less depreciation, which includes physical wear and tear, and obsolescence. 
	<p></p>
	<b>ACTUARY:</b> A person whose principal function is to make the technical calculations required for the pricing of insurance policies. 
	<p></p>
	<b>ADDITIONAL EXTENDED COVERAGE:</b> A second endorsement on the fire policy(fire and lightning with extended coverage) which insures the dwelling and/or contents against water damage from plumbing, etc.; boiler explosion; glass breakage; and damage by ice and snow, freezing, fall of trees, collapse, vandalism, vehicles owned by insured or tenants and landslide. 
	<p></p>
	<b>ADDITIONAL INSURED:</b> One who is protected by an insurance policy other than the named insured. Examples: In automobile insurance, one who drives the insured's car with his consent ordinarily is protected. In property insurance, this might be a co-owner, mortgagee, or lien holder. 
	<p></p>
	<b>ADJUSTER:</b> A person who investigates and settles losses for an insurance carrier or the insured. 
	<p></p>
	<b>ADVANCE PREMIUM:</b> Most companies give the insured the right of making premium payments in advance. 
	<p></p>
	<b>AGENCY:</b> An organization which solicits insurance for one or more carriers and may perform other functions such as issuing policies and adjusting losses. 
	<p></p>
	<b>AGENT:</b> 1. An individual who solicits insurance for one or more carriers and may perform other functions, such as issuing policies. 2. Agents of a direct writer are sales employees of one company only. 
	<p></p>
	<b>AGR & AGR LITE:</b> policies that insure revenue of the entire farm rather than an individual crop by guaranteeing a percentage of average gross farm revenue, including a small amount of livestock revenue. The policies use information from a producer's Schedule F tax forms, and current year expected farm revenue, to calculate policy revenue guarantee.
	<p></p>
	<b>ALL PHYSICAL LOSS FORM:</b> This coverage protects against loss from "all risks of physical loss" for dwellings subject to certain exclusions contained in the form. 
	<p></p>
	<b>ANNUAL POLICY:</b> Insurance policy written for a term of one year. 
	<p></p>
	<b>APPLICATION:</b> A request to a company for a policy. The application is a conditional offer to buy. It may be the policy named in the application or, if the applicant is substandard, it may be on a higher premium or other policy form. 
	<p></p>
	<b>APPRAISAL:</b> Determination of the value of property or the extent of damage by impartial experts. Many property insurance policies provide for "appraisals" where the company and the insured cannot agree on the amount or the extent of a loss. 
	<p></p>
	<b>APPROVED:</b> In fire insurance, usually means that the construction, equipment, preventive and protective devices meet established requirements for insurance. In many cases,"approved" construction results in reduced insurance premiums. 
	<p></p>
	<b>AREA:</b> A territorial subdivision, usually called "rating territory," within a given state used for rating purposes. 
	<p></p>
	<b>ARSON:</b> The willful and malicious burning of property, sometimes with the intent of defrauding an insurance company. 
	<p></p>
	<b>ASSETS:</b> All of the property owned by a carrier.  
	<p></p>
	<b>ASSIGNEE:</b> One to whom the legal ownership of a policy or a limited interest therein is transferred. 
	<p></p>
	<b>ASSIGNMENT:</b> The partial or complete transfer by a person of his right or interest in a policy to another person. The ability of a person to so assign the policy may be limited by law or individual circumstances. An assignment must be written, signed by the owner-policyholder whose interest is being transferred, properly attested, and the original or a certified copy must be filed with the insuring company. A valid assignment so filed is binding on the company. 
	<p></p>
	<b>ASSURANCE-INSURANCE:</b> These terms are today generally accepted as synonymous, although not originally so. The term "assurance" is used more commonly in Canada and Great Britain than in the United States.  

<a name=b>
	<div class="glossarytitle"> &nbsp;<b>B</b><a href=#home class=backtotop >back to top</a></div>
	<p></p>
	<b>BINDER:</b> A written or oral contract issued temporarily to place insurance in force when it is not possible to issue a new policy or endorse the existing policy immediately. A binder is subject to the premium and all the terms of the policy to be issued. 
	<p></p>
	<b>BODILY INJURY COVERAGE:</b> This coverage, often called "public liability insurance", protects an insured against legal liability for injury to the person of another arising from an accident. 
	<p></p>
	<b>BROAD FORM:</b> A policy affording more liberal benefits, or in fire insurance, an endorsement that grants broader or additional coverages to a basic policy; usually added to a standard fire and extended coverage policy. For example, on a dwelling policy, it usually adds the following: vandalism, glass breakage, falling trees, weight of ice, snow or sleet, collapse. If added to a commercial fire policy, it might include vandalism, falling objects, weight of ice, snow or sleet, and collapse. 
	<p></p>
	<b>BROKER:</b> A representative of the insured in placing insurance with companies. He is paid a commission by the company or its agent. Often a broker also is a licensed "agent" for one or more companies. 
	<p></p>
	<b>BUILDER'S RISK INSURANCE:</b> Insurance against loss resulting from damage to buildings and to materials incidental to construction, including machinery and equipment, while the buildings are under construction. 
	<p></p>
	<b>BURGLARY:</b> Breaking and entering into the premises of another for the purpose of stealing with visible signs of forced entry. 
	<p></p>
	<b>BUSINESS INSURANCE (LIFE):</b> Insurance concerned primarily with the protection of an insured's business or vocation. Business insurance protects a business against the loss of its valuable lives or key men; stabilizes the business through the establishment of better credit relations; and provides a practical plan for the retirement of business interest in the event of the death of one of the owners.  

<a name=c>
	<div class="glossarytitle"> &nbsp;<b>C</b><a href=#home class=backtotop >back to top</a></div>
	<p></p>
	<b>CANCELLABLE POLICY:</b> A policy which may be canceled by the company at any time by giving advance notice to the insured and refunding any unearned premium. 

	<p></p>
	<b>CANCELLATION:</b> The discontinuance of an insurance policy before its normal expiration date. 
	<p></p>
	<b>CAPITAL SUM:</b> A term in accident insurance to describe the amount payable for death or for loss of hands, feet or sight. Also called principal sum. 
	<p></p>
	<b>CARRIER:</b> The insurance company or the one who agrees to pay the losses. The carrier may be organized as a company, either stock, mutual, or reciprocal, or as an association of underwriters such as Lloyds. 
	<p></p>
	<b>CASUALTY INSURANCE:</b> A general class of insurance covering liability resulting from accidents and some types of property insurance. It includes among other coverages: automobile, workers compensation, employers liability, general liability, plate glass, theft and personal liability. It excludes life, fire and marine insurance, but, as ordinarily used, includes health insurance and fidelity and surety bonds. 
	<p></p>
	<b>CATASTROPHE REINSURANCE:</b> This is a form of insurance written on an excess of loss basis in order to improve the spread of risk against unknown concentrations of liability subject to one occurrence. A deductible is chosen at the amount necessary to reduce the probable frequency of loss to an acceptable level to the reinsurer, and severity of loss to alevel acceptable to the reinsured company. 
	<p></p>
	<b>CLAIM:</b> A request for payment of a loss which may come under the terms of an insurance contract. There are two types of claims. First party claim is one made by the policyholder in which the policyholder reports the claim directly to his company. A third party claim is one in which a person makes a claim against a policyholder of another company and payment, if any, is made by that company. 
	<p></p>
	<b>CLAIMANT:</b> One who makes a claim. 
	<p></p>
	<b>CLAIM SEVERITY:</b> The average cost per claim considering all claims under a certain coverage for a specified period. 
	<p></p>
	<b>CLAIMS-MADE COVERAGE:</b> Claims-made insurance is a type of liability protection that covers current legal obligations result for acts of the insured. It provides coverage to the insured for claims reported during the term of the policy. 
	<p></p>
	<b>COINSURANCE PROVISION:</b> An insurance provision for property coverage in which the policy holder must carry an amount of insurance that is at least equal to a set percentage of the value of the property in order to receive full payment of a loss. 
	<p></p>
	<b>COMBINED SINGLE LIMIT:</b>  Bodily Injury and Property Damage coverage expressed as one single amount of coverage. 
	<p></p>
	<b>COMMERCIAL FIRE:</b> This coverage insures all property not occupied as a residence, excluding farming and manufacturing, against loss by fire. 

	<p></p>
	<b>COMMON DISASTER CLAUSE: </b>In life insurance, this clause is designed to alleviate the hardship that can result if the insured and primary beneficiary dies at the same time or within a short period of time of each other. Usually the clause provides that if the primary beneficiary dies either before proof of the insured's death is submitted to the company, or within a stated period (usually 14 or 30 days after the insured's death), the proceeds will be paid to the contingent beneficiary. 
	<p></p>
	<b>COMPREHENSIVE COVERAGE (physical damage other than collision):</b>  Pays for damage to or loss of your automobile from causes other than accidents.  These include hail, vandalism, flood, fire, and theft. 
	<p></p>
	<b>CONCEALMENT:</b> The withholding of material facts from an insurer, either in applying for a policy or making a claim. 
	<p></p>
	<b>CONTENTS:</b> A term used to refer to the personal property contained in a building. It maybe household furniture, personal effects, or other types of personal property, movable in nature and not firmly affixed to the real property. 
	<p></p>
	<b>CONTINGENT BENEFICIARY:</b> A beneficiary whose right to receive depends upon the occurrence of a certain contingency – for example, the right to receive certain benefits only in the event that another named beneficiary dies prior to the time of payment. 
	<p></p>
	<b>CONTRACT:</b> In most cases, an insurance policy.  A policy is considered to be a contract between the insurance company and the policyholder. 
	<p></p>
	<b>CONTRIBUTORY NEGLIGENCE:</b> Carelessness of the injured person that helped to cause the accident in which he was injured. 
	<p></p>
	<b>COVERAGE:</b>  Another term for insurance.  Can be used to mean either the dollar amounts of insurance purchased (ex: $500,000 of liability coverage), or the type of loss covered(coverage for theft). 

<a name=d>
	<div class="glossarytitle"> &nbsp;<b>D</b><a href=#home class=backtotop >back to top</a></div>
	<p></p>
	<b>DAMAGES:</b> In a technical sense, damages refer to the money or compensation recoverable in a lawsuit by a party who has been injured in person or property or rights by the negligence of another. 
	<p></p>
	<b>DECLARATIONS PAGE:</b>  The page in a policy that shows the name and address of the insurer, the period of time a policy is in force, the amount of the premium, and the amount of coverage. 
	<p></p>
	<b>DEDUCTIBLE:</b> The amount an insured person must pay before the insurance company pays the remainder of each covered loss, up to the policy limits. 
	<p></p>
	<b>DEFERRED ANNUITY:</b> An annuity under which the annuity payment period is scheduled to begin at some future date. 
	<p></p>
	<b>DEPRECIATION:</b> A decrease in the value of property over a period of time due to wear and tear or obsolescence. Depreciation is used to determine the actual cash value of property at time of loss. (See ACTUAL CASH VALUE.) 
	<p></p>
	<b>DIRECT LOSS:</b> A loss where an insured peril is the proximate cause. If a windstorm blows the roof off a home, the windstorm is the insured peril causing the direct loss or damage. 
	<p></p>
	<b>DIVIDENDS:</b> A return to the policyholder of excess premium over losses and expenses at the end of the policy period. Dividends are authorized by the board of directors, and are payable to all participating policyholders of a specified class. 
	<p></p>
	<b>DOMESTIC CARRIER:</b> An insurance company organized in a given state is referred to in that state as a domestic carrier. 
	<p></p>
	<b>DOUBLE INDEMNITY:</b>  Payment of twice the basic benefit in the event of loss resulting from specified 

<a name=e>
	<div class="glossarytitle"> &nbsp;<b>E</b><a href=#home class=backtotop >back to top</a></div>
	<p></p>
	<b>EARNED PREMIUM:</b> The portion of the total policy premium that has been "used up" during a policy term.  With a one-year policy, half of the total premium has been earned after six months.  
	<p></p>
	<b>EFFECTIVE DATE:</b>  The date on which an insurance binder or policy goes into effect. 
	<p></p>
	<b>ENDORSEMENT:</b> A written agreement attached to a policy expanding or limiting the benefits otherwise payable under the policy.  Also called a "rider." 
	<p></p>
	<b>EQUIPMENT FLOATER:</b>  A property insurance coverage for equipment that is often moved from place to place. 
	<p></p>
	<b>ESTIMATED PREMIUM:</b>  A preliminary premium amount that could be adjusted based on a variance in exposures. 
	<p></p>
	<b>EVIDENCE OF INSURABILITY:</b>  To qualify for a particular policy at a particular price, companies have the right to ask for information about health and lifestyle.  An insurance company will use this information – the evidence of insurability – in deciding if your application for insurance is acceptable and at what premium rate. 
	<p></p>
	<b>EXCESS AND SURPLUS LINES INSURANCE:</b>  Coverage that is provided by insurers not licensed in states where the risk is located. 
	<p></p>
	<b>EXCESS LIABILITY POLICY:</b>  A policy that provides additional limits in excess of an underlying liability policy. 
	<p></p>
	<b>EXCLUSIONS:</b>  Items or conditions that are not covered by the general insurance contract. 
	<p></p>
	<b>EXPENSE RATIO:</b> The ratio of a company's operating expenses to premiums. 
	<p></p>
	<b>EXPIRATION DATE:</b> The specified date and time at which the policy terminates. 
	<p></p>
	<b>EXPOSURE:</b> Measure of vulnerability to loss, usually expressed in dollars or units. 
	<p></p>
	<b>EXTENDED COVERAGE:</b> An extension of the fire policy to cover the additional perils of windstorm, hail, explosion, or riot, attending a strike, civil commotion, aircraft, vehicle and smoke. 

	<p></p>
	<b>EXTENDED TERM INSURANCE OPTION:</b>  A policy provision that provides the option of continuing the existing amount of insurance as term insurance for as long a period of time as the contract's cash value will purchase. 

<a name=f>
	<div class="glossarytitle"> &nbsp;<b>F</b><a href=#home class=backtotop >back to top</a></div>
	<p></p>
	<b>FACE AMOUNT:</b> The amount covered by the terms of an insurance contract, usually found on the first page of the policy. 
	<p></p>
	<b>FIRST PARTY CLAIM:</b>  A claim filed by an insured against his or her own insurance policy. 
	<p></p>
	<b>FLAT CANCELLATION:</b>  The full cancellation of a policy as of the effective date of coverage, which requires the return of paid premium in full. 
	<p></p>
	<b>FLOATER:</b>  A type of insurance policy that covers property that is easily movable and provides additional coverage over what normal insurance policies do not. This can cover anything from jewelry to expensive stereo equipment. 
	<p></p>
	<b>FLOOD EXCLUSION:</b>  A provision in most all property insurance policies eliminating coverage for damage by flood and possibly other types of water damage, such as seepage and sewer backup. 
	<p></p>
	<b>FRANCHISE INSURANCE:</b> Individual life or health insurance policies issued to a small group of people having a common affiliation or interest. Same as wholesale insurance. 
	<p></p>
	<b>FREE LOOK:</b>  Trial period required in most states where policy owners have up to 20 days to examine their new policies with no obligation. 
	<p></p>
	<b>FULL COVERAGE:</b> Insurance which covers all losses, with no deductions, up to the amount of the insurance. 

<a name=g>
	<div class="glossarytitle"> &nbsp;<b>G</b><a href=#home class=backtotop >back to top</a></div>
	<p></p>
	<b>GENERAL LIABILITY:</b> A broad term meaning liability insurance, other than automobile, written to cover personal, professional and commercial risks. In respect to commercial liability, it protects business owners and operators from exposures such as liability arising from accidents resulting from the insured's premises or operations; products sold by the insured; operations completed by the insured, and contractual liability. 
	<p></p>
	<b>GRACE PERIOD:</b>  Period of time after the due date of a premium during which the policy remains in full force without penalty. 
	<p></p>
	<b>GROSS NEGLIGENCE:</b>  Willful and wanton misconduct. 
	<p></p>
	<b>GROSS VEHICLE WEIGHT (GVW):</b>  The weight specified by a manufacturer for the maximum total loaded weight of a single vehicle. 
	<p></p>
	<b>GROUP OF COMPANIES:</b>  Several insurance companies under common ownership and often common management. 

<a name=h>
	<div class="glossarytitle"> &nbsp;<b>H</b><a href=#home class=backtotop >back to top</a></div>
	<p></p>
	<b>HAZARD:</b>  A circumstance that increases the likelihood or probable severity of a loss.  For example, the storing of explosives in a home basement is a hazard that increases the probability of an explosion. 
	<p></p>
	<b>HOLD HARMLESS AGREEMENT:</b>  A contractual agreement that requires one contracting party to assume certain legal liabilities of the other party. 
	<p></p>
	<b>HOMEOWNER'S POLICY:</b> A policy in which Fire and extended or Broad coverage for dwelling and contents, residents' theft insurance, additional living expense, and personal liability insurance are combined. 

<a name=i>
	<div class="glossarytitle"> &nbsp;<b>I</b><a href=#home class=backtotop >back to top</a></div>
	<p></p>
	<b>INDEMNITY:</b>  Restoration to the victim of a loss by payment, repair or replacement. 
	<p></p>
	<b>INDEPENDENT ADJUSTER:</b>  A claims adjuster who provides adjustment services to insurance companies for a fee but is not employed by them. 
	<p></p>
	<b>INSURANCE POLICY:</b>  The printed form which serves as the contract between an insurer and an insured. 
	<p></p>
	<b>INSURED:</b>  The person or organization covered by an insurance policy. 
	<p></p>
	<b>INSURER:</b>  The insurance company.  The party that provides insurance coverage, typically through a contract of insurance. 

<a name=k>
	<div class="glossarytitle"> &nbsp;<b>K</b><a href=#home class=backtotop >back to top</a></div>
	<p></p>
	<b>KEY MAN INSURANCE:</b> Insurance used for a business purpose, usually to reimburse a corporation for the loss it sustains when an important member of the firm dies. 

<a name=l>
	<div class="glossarytitle"> &nbsp;<b>L</b><a href=#home class=backtotop >back to top</a></div>
	<p></p>
	<b>LAPSE:</b> The termination or discontinuance of a policy due to non-payment of a premium. 
	<p></p>
	<b>LIABILITY:</b>  Responsibility to another for one's negligence that results in injury or damage. 
	<p></p>
	<b>LIABILITY COVERAGE:</b>  Covers losses for which an insured is legally liable.  For homeowners insurance, for example, liability coverage protects the policyholder against financial loss if they are sued and found legally responsible for someone else's injury or property damage. 
	<p></p>
	<b>LICENSE – AGENT OR BROKER:</b> Certification, issued by the department of insurance, that an individual is qualified to solicit insurance applications for the period covered. 
	<p></p>
	<b>LICENSE – COMPANY:</b> Certification, issued by the department of insurance, stating that an insurance company is qualified to do business in the state. 
	<p></p>
	<b>LIFE INSURANCE:</b>  Insurance coverage that pays out a set amount of money to specified beneficiaries upon the death of the individual who is insured. 
	<p></p>
	<b>LIMIT OF LIABILITY:</b>  The most an insurance company agrees to pay in the case of a loss. 
	<p></p>
	<b>LIVESTOCK GROSS MARGIN (LGM):</b> provides protection for swine, cattle and dairy against the loss of gross margin, which is based on the difference between the market value of the livestock or milk minus the feed costs.
	<p></p>
	<b>LIVESTOCK RISK PROTECTION:</b> provides protection against declining livestock prices if the price, as specified in the policy, drops below the producer's selected coverage price.
	<p></p>
	<b>LOSS:</b>  The amount an insurance company pays for damages under the terms of the policy. 
	<p></p>
	<b>LOSS OF USE:</b>  A provision in homeowners and renters insurance policies that reimburses policyholders for the additional costs (housing, food, and other essentials) of having to live elsewhere while the home is being restored following a disaster. 
	<p></p>
	<b>LOSS PAYEE/LIEN HOLDER:</b>  A person or entity with a legally secured insurable interest in another's property, usually a financial institution that loaned money to buy designated property.  The property is the loan collateral.  If the property is damaged in an accident, loss payments will be made to you and to the loss payee on your policy. 
	<p></p>
	<b>LOSS PAYABLE CLAUSE:</b>  An insurance clause that authorizes loss payments to a person or entity having an insurable interest in the covered property. 
	<p></p>
	<b>LOSS RATIO:</b> Percentage of losses incurred against earned premiums for a given period. 

<a name=m>
	<div class="glossarytitle"> &nbsp;<b>M</b><a href=#home class=backtotop >back to top</a></div>
	<p></p>
	<b>MINIMUM PREMIUM:</b>  The lowest amount of premium to be charged for providing a particular insurance coverage. 
	<p></p>
	<b>MORTGAGE CLAUSE:</b>  Property insurance provisions granting protection for the mortgagee named in the policy.  It establishes that loss to mortgaged property is payable to the insured and to the mortgagee named in the policy. 
	<p></p>
	<b>MORTGAGE INSURANCE:</b> One of the basic uses of life insurance. Family heads buy mortgage insurance for the specific purpose of paying off any mortgage balance outstanding at their death. 
	<p></p>
	<b>MULTIPLE LINE:</b> A general term referring to fire and casualty insurance in general. A multiple line company writes auto, fire, health, commercial, boat owners, homeowners, individual fire, theft insurance. 
	<p></p>
	<b>MUTUAL INSURANCE COMPANIES:</b> Insurance companies without capital stock, owned by the policyholders for the purpose of sharing in the profits through dividends at the end of the policy year. 

<a name=n>
	<div class="glossarytitle"> &nbsp;<b>N</b><a href=#home class=backtotop >back to top</a></div>
	<p></p>
	<b>NAMED PERILS COVERAGE:</b>  A property insurance term referring to exact causes of loss specifically listed as covered. 
	<p></p>
	<b>NEGLIGENCE:</b> The failure to use the care that a reasonable and prudent person would have used under the same or similar circumstances. 
	<p></p>
	<b>NON-RENEWAL:</b>  a decision by an insurance company not to renew a policy. 

<a name=o>
	<div class="glossarytitle"> &nbsp;<b>O</b><a href=#home class=backtotop >back to top</a></div>
	<p></p>
	<b>OCCUPANCY:</b> This refers to the use of property. A home, for example, may have a real estate office in it. This dwelling would then have a "business occupancy." Occupancy plays a very important part in computing rates and determining the acceptance or rejection of risks. 
	<p></p>
	<b>OCCUPATIONAL HAZARD:</b> The danger of suffering a sickness or injury due to the hazards of an occupation. 
	<p></p>
	<b>OCCURRENCE:</b>  An event that results in an insured loss.  In some lines of business, such as liability, an occurrence is distinguished from an accident in that the loss doesn't have to be sudden and fortuitous and can result from continuous or repeated exposure which results in bodily injury or property damage neither expected nor intended by the insured. 
	<p></p>
	<b>OCCURRENCE COVERAGE:</b> Occurrence coverage is insurance for incidents of liability alleged to have occurred during the term of the policy, no matter when the claim is reported. 
	<p></p>
	<b>ORIGINAL AGE: </b> The age you were when you bought an insurance  policy. 
	<p></p>
	<b>OWNER:</b> The person who can legally exercise all rights and privileges in the life policy. This will usually be the insured, but may be any other party to whom proper transfer of these rights and privileges has been made. 

<a name=p>
	<div class="glossarytitle"> &nbsp;<b>P</b><a href=#home class=backtotop >back to top</a></div>
	<p></p>
	<b>PERIL:</b> The cause of possible loss, such as fire, windstorm, theft, explosion, or riot. 
	<p></p>
	<b>PERSISTENCY:</b> A term used to refer to the length of time insurance remains continuously in force. 
	<p></p>
	<b>PERSONAL PROPERTY:</b> This type of property is usually movable and easily transportable. On the other hand, real property generally is considered to be immovable such as land and things affixed to it. A rule of thumb definition for personal property is "everything other than real property." 
	<p></p>
	<b>PHYSICAL HAZARD:</b> This refers to the material, structural or operational features of the risk itself, apart from the persons owning or managing it. Electrical wiring, building construction, type of heating system, are examples of physical hazards. 
	<p></p>
	<b>POLICIES-IN-FORCE:</b> Policies written and recorded on the books of the carrier which are unexpired as of a given date. 
	<p></p>
	<b>POLICY:</b> The name generally used to mean the written contract of insurance. 
	<p></p>
	<b>POLICY FEE:</b> An amount charged by some companies in addition to the first regular premium. Also called "joiner's fee." 
	<p></p>
	<b>POLICYHOLDER:</b> One who owns an insurance policy. A mortgagee often is issued a copy of an insurance policy, or a certificate of insurance, at the request of the insured, but he is not a policyholder. 
	<p></p>
	<b>POLICY PERIOD:</b>  The period a policy is in force, from the beginning or effective date to the expiration date. 

	<p></p>
	<b>PREFERRED RISK:</b>  A positive characteristic of someone seeking to be insured.  Usually means a better likelihood for long life, and usually means a lower premium. 
	<p></p>
	<b>PREMISES:</b>  The location where coverage applies. 
	<p></p>
	<b>PREMIUM:</b>  The amount paid by an insured to an insurance company to obtain or maintain an insurance policy. 
	<p></p>
	<b>PREMIUMS-IN-FORCE:</b> Premium dollars which have been written and are unexpired on the books of the insurance carrier. 
	<p></p>
	<b>PREMIUM EARNED:</b>  The amount of the premium, paid for in advance, that has been" earned" by virtue of the fact that time has passed without a claim.  
	<p></p>
	<b>PREMIUM UNEARNED:</b> The portion of an insurance premium which applies to the unexpired portion of the policy period.  
	<p></p>
	<b>PREVENTED PLANTING COVERAGE:</b> provides a payment to growers when they are unable to plant their crops due to an insurable cause. Perils covered are weather related and include drought. Prevented planting coverage for the same insured cause of loss event can continue up to two years.
	<p></p>
	<b>PRIMARY POLICY:</b>  The insurance policy that pays first when you have a loss that is covered by more than one policy. 
	<p></p>
	<b>PROPERTY DAMAGE COVERAGE:</b> An agreement by an insurance carrier to protect an insured against legal liability for damage by his automobile to the property of another. 
	<p></p>
	<b>PROPERTY DAMAGE:</b>  In the general liability policy, a physical injury to property, resulting in the loss of use. 
	<p></p>
	<b>PROPERTY INSURANCE:</b>  First-party insurance for real and personal property against physical loss or damage. 
	<p></p>
	<b>PRO RATA CANCELLATION:</b>  The cancellation of an insurance policy with the return premium being the full proportion of premium for the unexpired term of the policy, without penalty for early cancellation. 
	<p></p>
	<b>PROVISIONS:</b>  Details of an insurance policy which explain the benefits, conditions and other features of the insurance contract. 
	<p></p>
	<b>PROXIMATE CAUSE:</b> The dominating cause of loss or damage; an unbroken chain of events between the occurrence of an insured peril and damage to property. As an illustration, water damage occurring from firefighting activities is covered under the fire policy because fire was the proximate cause of the loss. 

