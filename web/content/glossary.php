	<b>TERM:</b>  The length of time for which a policy or bond is in force. 
	<p></p>
	<b>TERMINATION:</b> The recording of a cancellation of an insurance policy. 
	<p></p>
	<b>THIRD PARTY CLAIM:</b> A claim filed against another person's insurance policy. 
	<p></p>
	<b>TOTAL LOSS:</b>  A loss of sufficient size about which it can be said there is no value left.  The complete destruction of the property.  The term also is used to mean a loss requiring the maximum amount a policy will pay. 

