	<b>UNREPORTED CLAIMS:</b> Accidents which have occurred but which have not been reported or recorded. 
	<p></p>
	<b>USUAL AND CUSTOMARY FEES:</b>  Charges for medical services that refer to the amount approved by the carrier for payment.  These charges may be based on rates usually charged by physicians and providers in your area; rate averages compiled by independent rating services; or rate averages compiled by the insurance company. 

