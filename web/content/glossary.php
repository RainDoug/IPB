	<p></p>
	<b>RATING TERRITORY:</b> A geographical grouping in which like hazards tend to equalize and permit the establishment of an equitable rate for the territory. 
	<p></p>
	<b>RENTS OR RENTAL VALUE:</b> Insurance against loss of the rental value of a property; protects against loss of rents resulting from an insured peril. 
	<p></p>
	<b>REAL PROPERTY:</b>  Real estate including buildings and vegetation. 
	<p></p>
	<b>REFUND:</b>  An amount of money returned to the policyholder for overpayment of premium or if the policyholder is due unearned premium.. 
	<p></p>
	<b>REINSTATEMENT:</b>  Putting a lapsed policy back in force by producing satisfactory evidence of insurability and paying any past-due premiums required. 
	<p></p>
	<b>RENEWAL POLICY:</b>  A policy issued to replace an expiring policy. 
	<p></p>
	<b>REPLACEMENT COST:</b> The dollar amount needed to replace damaged personal property or dwelling property without deducting for depreciation but limited by the maximum dollar amount shown on the declarations page of the policy. 
	<p></p>
	<b>RETURN PREMIUM:</b>  A portion of the premium returned to a policy owner as a result of cancellation, rate adjustment, or a calculation that an advance premium was in excess of the actual premium. 
	<p></p>
	<b>REVENUE PROTECTION (RP):</b> covers weather-related causes of loss, certain other unavoidable perils and price fluctuations. through a dollar guarantee. RP also provides prevented planting and replant protection. A projected price is used to calculate the premium, replant payments and prevent planting payments.
	<p></p>
	<b>RIDER:</b> Endorsement. Special provision added to an original policy contract. 
	<p></p>
	<b>RISK:</b> Chance of loss with respect to person, liability, or the property of the insured. Also used to mean "the insured." 

